package dev.isxander.ultralightfabric.mixins;

import dev.isxander.evergreenhud.event.EventManagerKt;
import net.minecraft.client.gui.hud.InGameHud;
import net.minecraft.client.util.math.MatrixStack;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(InGameHud.class)
public class MixinInGameHud {
    /**
     * Hook render hud event at the top layer
     */
    @Inject(method = "render", at = @At(value = "INVOKE", target = "Lnet/minecraft/client/gui/hud/InGameHud;renderStatusEffectOverlay(Lnet/minecraft/client/util/math/MatrixStack;)V", shift = At.Shift.AFTER))
    public void onHudRender(MatrixStack matrices, float tickDelta, CallbackInfo ci) {
        EventManagerKt.getEventBus().postConsumer((listener) -> listener.onRenderOverlay(matrices, tickDelta));
    }

}
